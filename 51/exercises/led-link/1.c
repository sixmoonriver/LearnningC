#include <reg52.h>    //包含单片机寄存器的头文件
#define uchar unsigned char
#define uint unsigned int

/* 函数申明 -----------------------------------------------*/

void delay(uint z);

/*
********************************************************************************
** 函数名称 ： main(void)
** 函数功能 ： 主函数
********************************************************************************
*/
void main()
{
	P1 = 0x00;
	delay(500);
	P1 = 0xFF;
	delay(500);
}

/*
********************************************************************************
** 函数名称 ： delay(uint z)
** 函数功能 ： 延时函数
********************************************************************************
*/
void delay(uint z)
{
    uchar j;
    for(z;z>0;z--)
        for(j=200;j>0;j--);
}